def build():
    items = []
    qid = 1
    for category, questions, hint in GROUPS:
        for q in questions:
            items.append({
                "id": f"q{qid:03d}",
                "category": category,
                "question": q,
                "eval_hint": hint,
            })
            qid += 1
    os.makedirs(os.path.dirname(OUT), exist_ok=True)
    with open(OUT, "w", encoding="utf-8") as f:
        for it in items:
            f.write(json.dumps(it, ensure_ascii=False) + "\n")
    # 분포 요약
    dist: dict[str, int] = {}
    for it in items:
        dist[it["category"]] = dist.get(it["category"], 0) + 1
    return len(items), dist


if __name__ == "__main__":
    n, dist = build()
    print(f"testset 생성: {n}건 -> {OUT}")
    for c, k in dist.items():
        print(f"  {c}: {k}")
